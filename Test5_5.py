# คํานวณหาพื้นที่ของสี่เหลี่ยมคางหมูโดยรับค่าข้อมูลตัวแปรทางคีย์บอร์ด
base1 = float(input("ป้อนความยาวฐานที่ 1 (เมตร): "))
base2 = float(input("ป้อนความยาวฐานที่ 2 (เมตร): "))
height = float(input("ป้อนความสูง (เมตร): "))
area = (base1 + base2) * height / 2
print("พื้นที่สี่เหลี่ยมคางหมู = %.3f ตารางเมตร"%(area))