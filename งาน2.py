# คำนวณราคาสินค้าทั้งหมดที่ลูกค้าต้องโอนเงิน โดยมีการบวกค่าจัดส่งแบบคงที่ (75 บาท)
price = float(input("ป้อนราคาสินค้า (บาท) : ")) ; 
quantity = int(input("ป้อนจำนวนสินค้า : ")) ; 
amount = price * quantity + 75 ; 

print("สินค้าราคา %.2f บาท \nจำนวน %d ชิ้น \nรวมทั้งหมด %.2f บาท" %(price,quantity,amount)) ; 
